    private void searchBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBTNActionPerformed
        // TODO add your handling code here:
       
        //event handling
        listMatchedCourses.clear();
        
        String name = nameTF.getText();
        String type = (String) typeCB.getSelectedItem();
              
        DefaultListModel listModel = new DefaultListModel();
        displayList.setModel(listModel);
        
        //for loop with use of ArrayLists
        for (SearchCourse c : listCourses) {
            
            //if statement regarding what the user enters and the outcomes
            if (name.equals("") && type.equals("")) {
                listMatchedCourses.add(c);
                listModel.addElement(c.getName() + " \t type:" + c.getType() + " \t price:" + c.getPrice());

            } else if (c.getName().contains(name) && c.getType().equals(type)) {
                listMatchedCourses.add(c);
                listModel.addElement(c.getName() + " \t type:" + c.getType()  +" \t price:" + c.getPrice());
            }
        }
          
    }//GEN-LAST:event_searchBTNActionPerformed

    private void sbnBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sbnBTNActionPerformed
        // TODO add your handling code here:

        //Event handling 
        DefaultListModel listModel = new DefaultListModel();
        displayList.setModel(listModel);

        //use of SORT_BY list the courses according by their names 
        SearchCourse.SORT_BY = "name";
        Collections.sort(listMatchedCourses );
     
        //for loop to list similar/matching course names 
        for (SearchCourse c : listMatchedCourses ) {
            listModel.addElement(c.getName() + " \t type:" + c.getType() +  " \t price:" + c.getPrice());
        }
        
    }//GEN-LAST:event_sbnBTNActionPerformed

